import turtle # turtle modülünü içeri aktar


timmy_the_turtle = turtle.Turtle() # Turtle sınıfından bir nesne oluştur

timmy_the_turtle.shape("turtle") # .shape() metodu ile timmy_the_turtle nesnesinin şeklini ayarla

timmy_the_turtle.color("green") # .color() metodu ile timmy_the_turtle nesnesinin rengini ayarla

timmy_the_turtle.forward(100) # .forward() metodu ile timmy_the_turtle nesnesini 100 birim ileriye götür

timmy_the_turtle.right(90) # .right() metodu ile timmy_the_turtle nesnesini 90 derece sağa döndür

